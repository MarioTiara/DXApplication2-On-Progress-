SELECT 
	B.REG_NO, A.WP_LAT, 
	A.WP_LON, A.WP_IO1, A.WP_SPEED, 
	A.WP_TIME, A.POLYGON, A.HEADING, 
	A.WP_PROPINSI, A.WP_KABUPATEN, 
	A.WP_KECAMATAN, A.WP_KELURAHAN, 
	A.WP_JALAN, A.WP_IO2, A.WP_IO3, 
	A.WP_IO4, B.MODEL_UNIT, A.EXT_POWER, 
	A.KM_ODO, B.CELLNO, A.SUHU01, A.SUHU02,
	C.IO1_Caption, C.IO1_On_Caption, C.IO1_Off_Caption, 
	C.IO2_Caption, C.IO2_On_Caption, C.IO2_Off_Caption, 
	C.IO3_Caption, C.IO3_On_Caption, C.IO3_Off_Caption, 
	C.IO4_Caption, C.IO4_On_Caption, C.IO4_Off_Caption, 
	C.Suhu01_Caption, C.Suhu02_Caption 
FROM WP_5b9241a1b00f41088d1e09a462b1d6b6 A WITH(NOLOCK,NOWAIT) 
	INNER JOIN VEHICLES B ON A.VEHICLE_SID = B.VEHICLE_SID 
	LEFT JOIN VehicleConfig C ON A.VEHICLE_SID = C.VehicleSid 
WHERE A.VEHICLE_SID= @VEHICLE_SID AND A.WP_TIME >= @FROM_DATE AND A.WP_TIME <= @TO_DATE ORDER BY A.WP_TIME ASC