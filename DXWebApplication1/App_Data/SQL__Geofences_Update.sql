UPDATE [dbo].[GEOFENCE]
   SET [COMPANY_SID] = @COMPANY_SID
      ,[DIVISION_SID] = @DIVISION_SID
      ,[SUBDIVISION_SID] = @SUBDIVISION_SID
      ,[GEOFENCE_NAME] = @GEOFENCE_NAME
      ,[GEOFENCE_CODE] = @GEOFENCE_CODE
      ,[GEOFENCE_TYPE] = @GEOFENCE_TYPE
      ,[GEOFENCE_SPEED] = @GEOFENCE_SPEED
      ,[GEOFENCE_SPEED2] = @GEOFENCE_SPEED2
      ,[GEOFENCE_ALERT] = @GEOFENCE_ALERT
      ,[GEOFENCE_GEOM] = geometry::STGeomFromText(@GEOFENCE_GEOM, 4326)
      ,[ACCOUNT_SID] = @ACCOUNT_SID
	  ,[IS_ACTIVE] = @IS_ACTIVE
	  ,[GEOFENCE_LAT] = @GEOFENCE_LAT
	  ,[GEOFENCE_LON] = @GEOFENCE_LON
	  ,[UPDATE_BY] = @UPDATE_BY
	  ,[UPDATE_DATE] = GETDATE()
 WHERE (GEOFENCE_SID = @GEOFENCE_SID)


